##Ejercicio de Aplicación 5: Conversión de Temperatura
##Define una función llamada convertir_celsius_a_fahrenheit que tome un valor en grados Celsius como parámetro y devuelva el equivalente en grados Fahrenheit.
##Llama a la función con un valor (por ejemplo, 25) y muestra el resultado.

def conversor(celsius):
    return (celsius*(9/5))+32
print(conversor(25))
